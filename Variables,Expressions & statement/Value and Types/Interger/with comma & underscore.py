# in many countries the large numbers comes with this (comma,) inbetween i.e 1,000,000 -> this won't work in python.
# it basically comes under the logical error (the code will run without error, but execute it wrongly).

print(1,000,000)

# instead of (comma,) if we use (underscore_) i.e 1_000_000, it will execute properly, without any logical errors.
# but the python will ignore the underscore in the output.
# this is used to make our code readable.

print(1_000_000)


# these "whole numbers", no matter if they are positive or negative, they are called integers in programming.